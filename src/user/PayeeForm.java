    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        int pID = Integer.valueOf(JOptionPane.showInputDialog("Type the Payee ID you need to find"));
        Payee pt = (Payee)pdao.get(pID);
        
            
        txtPID.setText(String.valueOf(pt.getpID()));
        txtFirstName.setText(pt.getFirstName());
        txtLastName.setText(pt.getLastName());
        txtAddress.setText(pt.getAddress());
        txtTelephone.setText(pt.getTelephone());
        txtEmail.setText(pt.getEmail());
        
        
        
        
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int pID = Integer.valueOf(JOptionPane.showInputDialog("Type the Payee ID to be deleted"));
        int result = pdao.delete(pID);
        
        
        if(result > 0)
            JOptionPane.showMessageDialog(rootPane, "The Payee is deleted");
        
        else
            JOptionPane.showMessageDialog(rootPane, "The Payee is not deleted");
    }//GEN-LAST:event_btnDeleteActionPerformed

