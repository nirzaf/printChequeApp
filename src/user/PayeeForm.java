    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        if(isValidate()){
            int pID=Integer.valueOf(txtPID.getText());
            String firstName=txtFirstName.getText();
            String lastName=txtLastName.getText();
            String address=txtAddress.getText();
            String telephone =txtTelephone.getText();
            String email=txtEmail.getText();

            Payee p=new Payee(pID, firstName, lastName, address, telephone, email);

            int result=pdao.add(p);
            if(result>0)
                JOptionPane.showMessageDialog(rootPane, "The Payee is added");
            else
                JOptionPane.showMessageDialog(rootPane, "The Payee is not added"); 
        }
        
        
        
        
        
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
            int pID=Integer.valueOf(txtPID.getText());
            String firstName=txtFirstName.getText();
            String lastName=txtLastName.getText();
            String address=txtAddress.getText();
            String telephone =txtTelephone.getText();
            String email=txtEmail.getText();
            
            Payee pt = new Payee(pID, firstName, lastName, address, telephone, email);
            int result = pdao.update(pt);
        
        if(result>0)
            JOptionPane.showMessageDialog(rootPane, "The Payee is updated");
        else
            JOptionPane.showMessageDialog(rootPane, "The Payee is not updated");
    }//GEN-LAST:event_btnUpdateActionPerformed

