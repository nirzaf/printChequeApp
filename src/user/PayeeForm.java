    private boolean isValidate(){        
        if (txtPID.getText().equals("")){
            JOptionPane.showMessageDialog(txtPID, "The Payee ID cannot be blank");
            txtPID.requestFocusInWindow();
            return false;
        }
        
        try{
             int pID=Integer.valueOf(txtPID.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(txtPID, "The Payee ID must be numeric");
            txtPID.requestFocusInWindow();
            return false;
        }
        
        if (txtFirstName.getText().equals("")){
            JOptionPane.showMessageDialog(txtFirstName, "The First Name cannot be blank");
            txtFirstName.requestFocusInWindow();
            return false;
        }
        
        if (txtLastName.getText().equals("")){
            JOptionPane.showMessageDialog(txtLastName, "The Last Name cannot be blank");
            txtLastName.requestFocusInWindow();
            return false;
        }
        
        if (txtAddress.getText().equals("")){
            JOptionPane.showMessageDialog(txtAddress, "The Address cannot be blank");
            txtAddress.requestFocusInWindow();
            return false;
        }
        
        if (txtTelephone.getText().equals("")){
            JOptionPane.showMessageDialog(txtTelephone, "The Telephone cannot be blank");
            txtTelephone.requestFocusInWindow();
            return false;
        }
        
        if (txtEmail.getText().equals("")){
            JOptionPane.showMessageDialog(txtFirstName, "The email cannot be blank");
            txtEmail.requestFocusInWindow();
            return false;
        }
        return true;
    }
    
    
